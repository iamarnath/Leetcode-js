var UndergroundSystem = function() {
    this.checkInDataMap = new Map();
    this.journeyDataMap = new Map();
};

/** 
 * @param {number} id 
 * @param {string} stationName 
 * @param {number} t
 * @return {void}
 */
UndergroundSystem.prototype.checkIn = function(id, stationName, t) {
     this.checkInDataMap.set(id, { stationName, t });
};

/** 
 * @param {number} id 
 * @param {string} stationName 
 * @param {number} t
 * @return {void}
 */
UndergroundSystem.prototype.checkOut = function(id, stationName, t) {
    //It first retrieves the check-in data for the user with the given id from the checkInDataMap.

    const checkInData = this.checkInDataMap.get(id);
  if (checkInData) {
    //If the check-in data is found (checkInData is not undefined), it calculates the key (routeKey) for the journey based on the starting station from the check-in data and
    // the ending station (stationName) for the check-out.
    const routeKey = `${checkInData.stationName}-${stationName}`;
    //It then retrieves the journey data for this route from the journeyDataMap. If no journey data exists for this route, 
    //it initializes a new object with totalTime set to 0 and tripCount set to 0.
    let journeyData = this.journeyDataMap.get(routeKey) || { totalTime: 0, tripCount: 0 };
    //It calculates the duration of the journey by subtracting the check-in time (checkInData.t) from the check-out time (t),
    // and updates the totalTime and tripCount in the journey data object.
    journeyData = {
      totalTime: journeyData.totalTime + (t - checkInData.t),
      tripCount: journeyData.tripCount + 1
    };
    //Finally, it sets the updated journey data back in the journeyDataMap and deletes the check-in data for this user from the checkInDataMap
    // since the user has now completed their journey.
    this.journeyDataMap.set(routeKey, journeyData);
    this.checkInDataMap.delete(id); // Remove check-in data after processing check-out
  }
};

/** 
 * @param {string} startStation 
 * @param {string} endStation
 * @return {number}
 */
UndergroundSystem.prototype.getAverageTime = function(startStation, endStation) {
    const routeKey = `${startStation}-${endStation}`;
const journeyData = this.journeyDataMap.get(routeKey);
if (!journeyData) throw new Error('No journey data found for this route.');

return journeyData.totalTime / journeyData.tripCount;
};

